    private void jButton1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton1MouseClicked
        datos.caminos(((Aeropuerto) jComboBox1.getSelectedItem()).getId().toString(), ((Aeropuerto) jComboBox2.getSelectedItem()).getId().toString(), (int) jComboBox3.getSelectedItem());
        int i = 0;
        for (TCamino camino : datos.getTodosLosCaminos().getCaminos()){
            String aerolinea = camino.getAerolinea().toString();
            String origen = camino.getOrigen().getEtiqueta().toString();
            String destino = ((Aeropuerto) jComboBox2.getSelectedItem()).getId().toString();
            String escala = escalas(camino.getOtrosVertices(),((Aeropuerto) jComboBox2.getSelectedItem()).getId());
            String costo = camino.getCostoTotal().toString();
            jTable1.setValueAt(aerolinea, i, 0);
            jTable1.setValueAt(origen, i, 1);
            jTable1.setValueAt(escala, i, 2);
            jTable1.setValueAt(destino, i, 3);
            jTable1.setValueAt(costo, i, 4);
            i++;
        }
        datos.getTodosLosCaminos().getCaminos().retainAll(new LinkedList());
    }//GEN-LAST:event_jButton1MouseClicked

    private String escalas(Collection<Comparable> camino, Comparable destino){
        camino.remove(destino);
        if (camino.isEmpty()){
            return "";
        }
        return camino.toString();
    }
    
